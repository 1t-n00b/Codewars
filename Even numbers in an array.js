function evenNumbers(array, number) {
  // good luck
  let even_arr = [];
  let end = 0;
  for (let i= array.length-1; i>=0; i--) {
    if  (array[i] % 2 === 0) {
      end++;
      even_arr.unshift(array[i]);
      if (number==end) {break;} 
    }
  }
 // console.log(even_arr);
  return even_arr;
}
